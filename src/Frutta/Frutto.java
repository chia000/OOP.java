package Frutta;

public class Frutto {
    private String tipo;
    private int codice;

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public void setCodice(int codice) {
        this.codice = codice;
    }

    public String getTipo() {
        return tipo;
    }

    public int getCodice() {
        return codice;
    }
}
